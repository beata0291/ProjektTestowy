public class Human {
    public double age;
    public double weight;
    public String name;
    public boolean isMale;

    public Human(double weight, double age, String name, boolean isMale) {
        this.age = age;
        this.weight = weight;
        this.age = age;
        this.name = name;
        this.isMale = isMale;

    }

    public boolean isMale() {
        return isMale;
    }
    public String getName() {
        return name;
}

    }



